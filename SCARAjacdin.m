function J=SCARAjacdin(Q,L)
    l1=L(2); l2=L(3); l3=L(4);
    
    J=zeros(16,4);
    J(1,2)= -l1*sin(Q(2))-l2*sin(Q(2)+Q(3))-l3*sin(Q(2)+Q(3)+Q(4));
    J(1,3)= -l2*sin(Q(2)+Q(3))-l3*sin(Q(2)+Q(3)+Q(4));
    J(1,4)= -l3*sin(Q(2)+Q(3)+Q(4));
   
    J(2,2)= l1*cos(Q(2))+l2*cos(Q(2)+Q(3))+l3*cos(Q(2)+Q(3)+Q(4));
    J(2,3)= l2*cos(Q(2)+Q(3))+l3*cos(Q(2)+Q(3)+Q(4));
    J(2,4)= l3*cos(Q(2)+Q(3)+Q(4));
    
    J(3,1)=1;

    J(4,2)=1;
    J(4,3)=1;
    J(4,4)=1;
    
    J(5,2)= -l1*sin(Q(2))-l2/2*sin(Q(2)+Q(3));
    J(5,3)= -l2/2*sin(Q(2)+Q(3));
    
    J(6,2)= l1*cos(Q(2))+l2/2*cos(Q(2)+Q(3));
    J(6,3)= l2/2*cos(Q(2)+Q(3));
    
    J(7,1)=1;
  
    J(8,2)=1;
    J(8,3)=1;

    J(9,2)= -l1/2*sin(Q(2));
    
    J(10,2)= l1/2*cos(Q(2));
    
    J(11,1)=1;
    
    J(12,2)=1;
    
    J(16,1)=1; 
end